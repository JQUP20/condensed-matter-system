      <ul>
        <li>效率：>28%（单结）</li>
        <li>直接带隙，吸收系数高</li>
        <li>成本高，主要用于空间应用</li>
      </ul>

      <p><strong>铜铟镓硒(CIGS)</strong>：</p>
      <ul>
